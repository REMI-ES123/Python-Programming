a = 15
b = 4

#Addition
print("Addition:",a+b)
#Subtraction
print("Subtraction:",a-b)
#Multiplication
print("Multiplication:",a*b)
#Division
print("Division:",a/b)
#Floor division (integer floor) //
print("floor division:",a//b)
#Modulo (remainder) %
print("Modulo:",a%b)
#Exponentiation (power) **
print("Exponent:",a**b)