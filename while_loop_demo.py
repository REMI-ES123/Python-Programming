#count from 1 to 5
counter = 1
while counter <= 5:
    print(counter)
    counter = counter + 1

#Example 2 - Countdown from 5
count = 5
while count > 0:
    print(count)
    count = count - 1

# Example 3 - Sum of first n numbers
number = 1
total = 0
while number <= 5:
    total = total + number
    number += 1
print("Sum is :  ", total)